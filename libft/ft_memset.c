#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*a;
	char	b;

	i = 0;
	a = (char *)s;
	b = (char)c;
	while (i < n)
	{
		a[i] = b;
		i++;
	}
	return (s);
}
